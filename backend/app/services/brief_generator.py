# ---------------------------------------------------------------------------
# GOST PDF extraction (cached — only parses once per process)
# ---------------------------------------------------------------------------

@lru_cache(maxsize=1)
def _extract_gost_text() -> str:
    if not _GOST_PDF.exists():
        logger.warning("GOST PDF not found at %s", _GOST_PDF)
        return ""
    try:
        text = pdf_extract_text(str(_GOST_PDF))
        lines = [l.rstrip() for l in text.splitlines()]
        text = "\n".join(lines)
        text = re.sub(r"\n{3,}", "\n\n", text).strip()
        logger.info("GOST PDF loaded: %d chars", len(text))
        return text
    except Exception as exc:
        logger.error("Failed to read GOST PDF: %s", exc)
        return ""


# ---------------------------------------------------------------------------
# PDF renderer
# ---------------------------------------------------------------------------

class BriefPDF(FPDF):
    def __init__(self):
        super().__init__()
        fp = _FONT_DIR / "DejaVuSans.ttf"
        fb = _FONT_DIR / "DejaVuSans-Bold.ttf"
        if fp.exists():
            self.add_font("DejaVu", "", str(fp), uni=True)
            if fb.exists():
                self.add_font("DejaVu", "B", str(fb), uni=True)
            self._font_family = "DejaVu"
        else:
            self._font_family = "Helvetica"

    def header(self):
        self.set_font(self._font_family, "B", 14)
        self.cell(
            0, 10,
            "MoodBrief \u2014 \u0422\u0435\u0445\u043d\u0438\u0447\u0435\u0441\u043a\u043e\u0435"
            " \u0437\u0430\u0434\u0430\u043d\u0438\u0435",
            ln=True, align="C",
        )
        self.ln(4)

    def footer(self):
        self.set_y(-15)
        self.set_font(self._font_family, "", 8)
        self.set_text_color(150, 150, 150)
        self.cell(0, 10, "\u0421\u0442\u0440. " + str(self.page_no()) + "/{nb}", align="C")


