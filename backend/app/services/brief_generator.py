def generate_brief_pdf(markdown_text: str, project_name: str = "brief") -> str:
    pdf = BriefPDF()
    pdf.alias_nb_pages()
    pdf.add_page()
    pdf.set_auto_page_break(auto=True, margin=15)
    font = pdf._font_family

    content_lines = markdown_text.split("\n")
    i = 0
    while i < len(content_lines):
        line = content_lines[i]
        stripped = line.strip()

        if pdf.get_x() > pdf.l_margin:
            pdf.ln()

        if stripped.startswith("#### "):
            pdf.set_font(font, "B", 10); pdf.ln(1)
            pdf.multi_cell(0, 5, stripped[5:]); pdf.ln(1)

        elif stripped.startswith("### "):
            pdf.set_font(font, "B", 11); pdf.ln(1)
            pdf.multi_cell(0, 6, stripped[4:]); pdf.ln(1)

        elif stripped.startswith("## "):
            pdf.set_font(font, "B", 13); pdf.ln(2)
            pdf.multi_cell(0, 7, stripped[3:]); pdf.ln(2)

        elif stripped.startswith("# "):
            pdf.set_font(font, "B", 16)
            pdf.multi_cell(0, 8, stripped[2:]); pdf.ln(3)

        elif stripped.startswith("---"):
            pdf.ln(3)
            pdf.set_draw_color(200, 200, 200)
            pdf.line(10, pdf.get_y(), 200, pdf.get_y())
            pdf.ln(3)

        elif stripped.startswith("|"):
            if all(c in "| -:" for c in stripped):
                i += 1; continue
            table_data: list[list[str]] = []
            while i < len(content_lines) and content_lines[i].strip().startswith("|"):
                rs = content_lines[i].strip()
                if not all(c in "| -:" for c in rs):
                    cells = [c.strip() for c in rs.split("|")[1:-1]]
                    if cells:
                        table_data.append(cells)
                i += 1
            if table_data:
                pdf.set_font(font, "", 9)
                with pdf.table(
                    borders_layout="SINGLE_TOP_LINE", cell_fill_color=245,
                    cell_fill_mode="ROWS", line_height=5, text_align="LEFT", width=190,
                ) as table:
                    for row_cells in table_data:
                        row = table.row()
                        for cell_text in row_cells:
                            row.cell(re.sub(r"[`*_]", "", cell_text))
                pdf.ln(2)
            continue

        elif stripped.startswith("- ") or stripped.startswith("* "):
            pdf.set_font(font, "", 10)
            pdf.cell(5, 6, "  \u2013")
            pdf.multi_cell(185, 6, re.sub(r"[`*_]", "", stripped[2:]))
            if pdf.get_x() > pdf.l_margin:
                pdf.ln(1)

        elif stripped.startswith("_") and stripped.endswith("_") and len(stripped) > 2:
            pdf.set_font(font, "", 9); pdf.set_text_color(120, 120, 120)
            pdf.multi_cell(0, 5, stripped.strip("_"))
            pdf.set_text_color(0, 0, 0); pdf.ln(1)

        elif not stripped:
            pdf.ln(3)

        else:
            is_numbered = bool(re.match(r"^\d+(\.\d+)*\.?\s+\S", stripped))
            clean = re.sub(r"\*\*(.+?)\*\*", r"\1", stripped)
            clean = re.sub(r"[`_]", "", clean)
            if is_numbered:
                pdf.set_font(font, "B", 11); pdf.ln(1)
                pdf.multi_cell(0, 6, clean); pdf.ln(1)
            else:
                pdf.set_font(font, "", 10)
                pdf.multi_cell(0, 6, clean)

        i += 1

    slug = re.sub(r"[^a-zA-Z0-9]", "_", project_name)[:40]
    filename = f"{slug}_{uuid.uuid4().hex[:8]}.pdf"
    pdf.output(str(GENERATED_DIR / filename))
    return filename
