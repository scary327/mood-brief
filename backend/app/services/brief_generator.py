async def generate_brief_via_ai(
    tags: list[ImageTagSet],
    project_name: str,
    description: str = "",
    selected_fonts: list[str] | None = None,
    selected_colors: list[str] | None = None,
    template_id: str = "standard",
    user_notes: str = "",
) -> str:
    merged = _collect_all_tags(tags)
    today_ru = datetime.now(timezone.utc).strftime("%d.%m.%Y")

    if (template_id or "standard") == "gost":
        return await _generate_gost(
            project_name=project_name,
            description=description or user_notes,
            today=today_ru,
            colors=selected_colors or [],
            fonts=selected_fonts or [],
            tags=merged,
        )

    # Standard / Creative
    context_str = json.dumps(
        {
            "project_name": project_name,
            "description": description,
            "selected_fonts": selected_fonts or [],
            "selected_colors": selected_colors or [],
            "user_notes": user_notes,
            "visual_tags": merged,
        },
        ensure_ascii=False,
        indent=2,
    )

    if (template_id or "standard") == "creative":
        template_text = _TEMPLATE_CREATIVE
    else:
        template_text = _TEMPLATE_STANDARD

    user_message = (
        f"{template_text}\n\n---\n"
        f"ДАННЫЕ О ПРОЕКТЕ:\n\n{context_str}\n\n"
        f"_Дата составления: {today_ru}_"
    )

    return await _call_ai(system=_BASE_SYSTEM, user=user_message)


async def _generate_gost(
    project_name: str,
    description: str,
    today: str,
    colors: list[str],
    fonts: list[str],
    tags: dict[str, list[str]],
) -> str:
    """Build skeleton, send to AI for {{AI:}} block filling."""
    skeleton = _build_gost_skeleton(project_name, description, today, colors, fonts, tags)

    gost_ref = _extract_gost_text()
    system = (
        _BASE_SYSTEM
        + "\n\n"
        "Ты получишь Markdown-документ, в котором некоторые места помечены "
        "тегами {{AI: инструкция}}. Твоя задача:\n"
        "1. Заменить КАЖДЫЙ тег {{AI: ...}} реальным профессиональным текстом "
        "согласно инструкции внутри тега.\n"
        "2. Вернуть ПОЛНЫЙ документ — весь текст до и после тегов сохраняется "
        "без изменений.\n"
        "3. В готовом документе не должно быть ни одного {{AI:}} тега — все "
        "заменены содержанием.\n"
        "4. Строки с подчёркиваниями _________________________ НЕ ТРОГАЙ — "
        "они предназначены для ручного заполнения."
    )
    if gost_ref:
        system += (
            "\n\nСПРАВОЧНО — текст ГОСТ 34.602-2020 (используй для соответствия "
            "требованиям стандарта):\n"
            + gost_ref[:10000]
        )

    return await _call_ai(system=system, user=skeleton)


async def _call_ai(system: str, user: str) -> str:
    payload = {
        "model": settings.OPENROUTER_TEXT_MODEL,
        "messages": [
            {"role": "system", "content": system},
            {"role": "user", "content": user},
        ],
        "max_tokens": 6000,
        "temperature": 0.2,
    }
    headers = {
        "Authorization": f"Bearer {settings.OPENROUTER_API_KEY}",
        "Content-Type": "application/json",
        "HTTP-Referer": "https://moodbrief.app",
        "X-Title": "MoodBrief",
    }
    async with httpx.AsyncClient(timeout=300.0) as client:
        resp = await client.post(
            f"{settings.OPENROUTER_BASE_URL}/chat/completions",
            json=payload,
            headers=headers,
        )
        resp.raise_for_status()
        data = resp.json()

    raw = data["choices"][0]["message"]["content"].strip()
    if raw.startswith("```"):
        lines = raw.split("\n")
        raw = "\n".join(lines[1:]) if len(lines) > 1 else raw
        if raw.endswith("```"):
            raw = raw[: -3]
    return raw.strip()


