async def _call_ai(system: str, user: str, max_tokens: int = 6000) -> str:
    payload = {
        "messages": [
            {"role": "system", "content": system},
            {"role": "user", "content": user},
        ],
        "max_tokens": max_tokens,
        "temperature": 0.2,
    }
    data, used_model = await chat_completion_with_fallback(
        payload=payload,
        models=settings.text_model_chain(),
        timeout=300.0,
    )
    logger.info("Brief text generated by %s", used_model)

    raw = data["choices"][0]["message"]["content"].strip()
    finish_reason = data["choices"][0].get("finish_reason", "stop")

    # Strip accidental code fence wrapping
    if raw.startswith("```"):
        lines = raw.split("\n")
        raw = "\n".join(lines[1:]) if len(lines) > 1 else raw
        if raw.endswith("```"):
            raw = raw[: -3]
    raw = raw.strip()

    # If output was cut by token limit, trim to the last clean ending
    # so we don't return a half-sentence or a broken table row.
    if finish_reason == "length":
        logger.warning("AI response hit token limit — trimming to last clean boundary")
        # Priority: last complete Markdown section (##) → last paragraph → last sentence
        # Walk backwards through double-newline blocks
        blocks = raw.rsplit("\n\n", 1)
        if len(blocks) == 2 and len(blocks[0]) > len(raw) * 0.5:
            # Discard last block only if it looks incomplete (no sentence-ending punctuation)
            last_block = blocks[1].rstrip()
            ends_cleanly = last_block.endswith((".", "!", "?", "|", ";")) or \
                           last_block.startswith(("|", "-", "*", "#"))
            if not ends_cleanly:
                raw = blocks[0].rstrip()

    return raw


